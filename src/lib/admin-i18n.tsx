interface AdminI18nContextType {
  locale: AdminLocale;
  setLocale: (locale: AdminLocale) => void;
  t: (key: string) => string;
}

const AdminI18nContext = createContext<AdminI18nContextType>({
  locale: "en",
  setLocale: () => {},
  t: (key: string) => key,
});

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AdminLocale>("en");

  useEffect(() => {
    const saved = localStorage.getItem("admin-locale");
    if (saved === "en" || saved === "ru") {
      setLocaleState(saved);
    }
  }, []);

  const setLocale = useCallback((l: AdminLocale) => {
    setLocaleState(l);
    localStorage.setItem("admin-locale", l);
  }, []);

  const t = useCallback((key: string): string => {
    return translations[locale][key] || translations.en[key] || key;
  }, [locale]);

  return (
    <AdminI18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </AdminI18nContext.Provider>
  );
}

export function useAdminI18n() {
  return useContext(AdminI18nContext);
}
